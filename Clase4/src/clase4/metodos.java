package clase4;

public class metodos {
    //funcion estatica para resta
    public static int resta(int num1, int num2){
        return num1-num2;
    }
    //guncion estatica para suma
    public static int suma(int num1, int num2){
        return num1+num2;
    }
    //funcion estatica para multiplicacion
    public static int multiplicacion(int num1, int num2){
        return num1*num2;
    }
    //funcion estatica para division
    public static double division(double num1,int num2){
        return num1/num2;
    }
    //funcion mod
    public int resto(int num1,int num2){
       return num1%num2; 
    }
}
